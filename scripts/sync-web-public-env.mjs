const parseEnvFile = (source) => {
  const values = new Map();

  for (const rawLine of source.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;

    const separatorIndex = line.indexOf("=");
    if (separatorIndex === -1) continue;

    const key = line.slice(0, separatorIndex).trim();
    let value = line.slice(separatorIndex + 1).trim();
    if (
      (value.startsWith("\"") && value.endsWith("\"")) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    values.set(key, value);
  }

  return values;
};

const quote = (value) => JSON.stringify(value ?? "");

const readEnvMap = (filePath) => {
  if (!fs.existsSync(filePath)) {
    return new Map();
  }
  return parseEnvFile(fs.readFileSync(filePath, "utf8"));
};

const rootEnv = readEnvMap(rootEnvPath);
const currentWebEnv = readEnvMap(webEnvPath);
const exampleEnv = readEnvMap(webExamplePath);

const resolveValue = (key) =>
  process.env[key]?.trim() ||
  rootEnv.get(key)?.trim() ||
  currentWebEnv.get(key)?.trim() ||
  exampleEnv.get(key)?.trim() ||
  "";

