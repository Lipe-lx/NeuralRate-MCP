const parseEnvFile = (source) => {
  const values = new Map();

  for (const rawLine of source.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;
    const separatorIndex = line.indexOf("=");
    if (separatorIndex === -1) continue;

    const key = line.slice(0, separatorIndex).trim();
    let value = line.slice(separatorIndex + 1).trim();
    if (
      (value.startsWith("\"") && value.endsWith("\"")) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    values.set(key, value);
  }

  return values;
};

const quote = (value) => JSON.stringify(value ?? "");

const writeEnvFile = (filePath, title, bindings, values) => {
  const lines = [`# Generated by scripts/sync-runtime-envs.mjs`, `# ${title}`, ""];

  for (const binding of bindings) {
    const key = typeof binding === "string" ? binding : binding.target;
    const sources = typeof binding === "string" ? [binding] : binding.sources;
    const value = sources
      .map((sourceKey) => values.get(sourceKey))
      .find((candidate) => typeof candidate === "string" && candidate.length > 0);
    if (typeof value !== "string" || value.length === 0) continue;
    lines.push(`${key}=${quote(value)}`);
  }

  lines.push("");
  fs.writeFileSync(filePath, `${lines.join("\n")}`);
  console.log(`[runtime-env] wrote ${path.relative(repoRoot, filePath)}`);
};

